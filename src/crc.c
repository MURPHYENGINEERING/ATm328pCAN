/*******************************************************************************
 * Compute an 8-bit checksum of the given buffer, adding that value to the given
 * checksum. The checksum is computed by summing the bytes in the buffer.
 * \param[in] buf           The buffer whose contents will be checksummed.
 * \param[in] len           Length in bytes of the buffer.
 * \param[in] running_val   An existing checksum to add to.
 ******************************************************************************/
U8_T crc_compute_checksum8(U8_T* buf, SIZE_T len, U8_T running_val)
{
    SIZE_T i;

    for (i = 0u; i < len; ++i) {
        running_val += buf[i];
    }

    return running_val;
}

